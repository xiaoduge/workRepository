/*******************************************************************************
* Function Name  : Initialize_TSM
* Description    : Initialize Tank Sanitization Module
* Input          : None
* Output         : None
* Return         : None
* Attention      : None
*******************************************************************************/
void Initialize_TSM(void)
{
  LED_Power_On;

  LanguageNo = LanguageNo_Simplified_Chinese;
  Operate_24LC01B(_24LC01_Read, Address_Language, 0);
  LanguageNo = RAM_24LC01B;
  if ((LanguageNo != 1) && (LanguageNo != 2))
  {
    LanguageNo = 1;
  }
  Operate_24LC01B(_24LC01_Read, Address_UV_Loop_Time, 0);
  UV_Loop_Time = RAM_24LC01B;
  Operate_24LC01B(_24LC01_Read, Address_UV_Life_MSB, 0);
  UV_Life = RAM_24LC01B << 8;
  Operate_24LC01B(_24LC01_Read, Address_UV_Life_LSB, 0);
  UV_Life = UV_Life + RAM_24LC01B;
  Operate_24LC01B(_24LC01_Read, Address_UV_Used_Time_MSB, 0);
  UV_Used_Time = RAM_24LC01B << 8;
  Operate_24LC01B(_24LC01_Read, Address_UV_Used_Time_LSB, 0);
  UV_Used_Time = UV_Used_Time + RAM_24LC01B;

  PageNo = 1;
  Page_2No = 1;

  Display_Logo();
  Systick_Delay_N_second(1);

  Display_Product_Title();
  Systick_Delay_N_second(1);

  Operate_PCF8563(PCF8563_Read, PCF8563_Register_Hours, 0);//Read Hours
  Value_Hour = ((RAM_PCF8563 >> 4) & 0x03) * 10 + (RAM_PCF8563 & 0x0F);
  Operate_PCF8563(PCF8563_Read, PCF8563_Register_Minutes, 0);//Read Minutes
  Value_Minute = ((RAM_PCF8563 >> 4) & 0x07) * 10 + (RAM_PCF8563 & 0x0F);
  Operate_PCF8563(PCF8563_Read, PCF8563_Register_VL_Seconds, 0);//Read Seconds
  Value_Second = ((RAM_PCF8563 >> 4) & 0x07) * 10 + (RAM_PCF8563 & 0x0F);
  Operate_PCF8563(PCF8563_Read, PCF8563_Register_Years, 0);//Read Year
  Value_Year = ((RAM_PCF8563 >> 4) & 0x07) * 10 + (RAM_PCF8563 & 0x0F);
  Operate_PCF8563(PCF8563_Read, PCF8563_Register_Century_Months, 0);//Read Month
  Value_Month = ((RAM_PCF8563 >> 4) & 0x01) * 10 + (RAM_PCF8563 & 0x0F);
  Operate_PCF8563(PCF8563_Read, PCF8563_Register_Days, 0);//Read Day
  Value_Day = ((RAM_PCF8563 >> 4) & 0x03) * 10 + (RAM_PCF8563 & 0x0F);

  LCM12232B_CLS();
  Loading_Page_1();

  Timer = 0;
  Start_Flag = 1;
}

