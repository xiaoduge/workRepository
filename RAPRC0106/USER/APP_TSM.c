/*******************************************************************************
* Function Name  : Refresh_Page
* Description    : None
* Input          : None
* Output         : None
* Return         : None
* Attention      : None
*******************************************************************************/
void Refresh_Page(void)
{
  if ((RefreshDisplayPageNo != 0) && (PageNo != 22))
  {
    Operate_PCF8563(PCF8563_Read, PCF8563_Register_Hours, 0);//Read Hours
    Value_Hour = ((RAM_PCF8563 >> 4) & 0x03) * 10 + (RAM_PCF8563 & 0x0F);
    Operate_PCF8563(PCF8563_Read, PCF8563_Register_Minutes, 0);//Read Minutes
    Value_Minute = ((RAM_PCF8563 >> 4) & 0x07) * 10 + (RAM_PCF8563 & 0x0F);
    Operate_PCF8563(PCF8563_Read, PCF8563_Register_VL_Seconds, 0);//Read Seconds
    Value_Second = ((RAM_PCF8563 >> 4) & 0x07) * 10 + (RAM_PCF8563 & 0x0F);
    Operate_PCF8563(PCF8563_Read, PCF8563_Register_Years, 0);//Read Year
    Value_Year = ((RAM_PCF8563 >> 4) & 0x07) * 10 + (RAM_PCF8563 & 0x0F);
    Operate_PCF8563(PCF8563_Read, PCF8563_Register_Century_Months, 0);//Read Month
    Value_Month = ((RAM_PCF8563 >> 4) & 0x01) * 10 + (RAM_PCF8563 & 0x0F);
    Operate_PCF8563(PCF8563_Read, PCF8563_Register_Days, 0);//Read Day
    Value_Day = ((RAM_PCF8563 >> 4) & 0x03) * 10 + (RAM_PCF8563 & 0x0F);

    if ((Value_Minute == UV_Loop_Time) && (Value_Second == 0))
    {
      UV_Used_Time++;
      Operate_24LC01B(_24LC01_Write, Address_UV_Used_Time_MSB, (char)(UV_Used_Time >> 8));
      Systick_Delay_N_millisecond(100);
      Operate_24LC01B(_24LC01_Write, Address_UV_Used_Time_LSB, (char)UV_Used_Time);
      Systick_Delay_N_millisecond(100);
    }
  }

  if (PageNo == 1)
  {
    switch (RefreshDisplayPageNo)
    {
      case 1://Main
        LCM12232B_CLS();
        Loading_Page_1();
        RefreshDisplayPageNo = 0;
        break;
      case 11://Test
        LCM12232B_CLS();
        Display_Page_UV_Test();
        break;
      case 12://UV Change
        LCM12232B_CLS();
        Display_Page_UV_Change();
        break;
      case 13://UV Error
        LCM12232B_CLS();
        Display_Page_UV_Error();
        break;
    }

    if ((RefreshDisplayPageNo == 12) || (RefreshDisplayPageNo == 13))
    {
      LED_Operate_Off;
      LED_Error_On;
    }
    else
      if (((Test_Flag != 0) && (~(Test_Timer >= Test_Time))) || ((~(Test_Flag != 0) && (UVStatusNo == 1))))
      {
        LED_Operate_On;
        LED_Error_Off;
      }
      else
      {
        LED_Operate_Off;
        LED_Error_Off;
      }
  }
}

/*******************************************************************************
* Function Name  : EXTI3_IRQHandler
* Description    : This function handles External lines 3 interrupt request
* Input          : None
* Output         : None
* Return         : None
* Attention      : None
*******************************************************************************/
void EXTI15_10_IRQHandler(void)
{
  if (EXTI_GetITStatus(EXTI_Line13) != RESET)
  {
    EXTI_ClearITPendingBit(EXTI_Line13);

    if (Start_Flag != 0)
    {
      if (PageNo == 1)
      {
        if (Test_Flag != 0)
        {
          if ((DI1 != 0) && (Test_Timer > 3))
          {
            RefreshDisplayPageNo = 13;
          }
          else
          {
            RefreshDisplayPageNo = 11;
          }

          if (Test_Timer >= Test_Time)
          {
            Test_Flag = 0;
            DO1_Reset;
          }
          else
          {
            Test_Timer++;
            DO1_Set;
          }
        }
        else
        {
          if ((DI1 != 0) && ((Value_Hour % 2 == 0) && (UV_Loop_Time > Value_Minute)) && (RefreshDisplayPageNo != 13))
          {
            if (Timer < 3)
            {
              Timer ++;
            }
            else
            {
              RefreshDisplayPageNo = 13;
            }
          }
          else
            if ((DI1 == 0) && (UV_Loop_Time > Value_Minute) && (RefreshDisplayPageNo == 13))
            {
                Timer = 0;
                RefreshDisplayPageNo = 0;
            }
            else
              if (RefreshDisplayPageNo != 13)
              {
                if (((UV_Used_Time + 12) / 24) >= UV_Life)
                {
                  RefreshDisplayPageNo = 12;
                }
                else
                {
                  RefreshDisplayPageNo = 1;
                }
              }

          if ((Value_Hour % 2 == 0) && (UV_Loop_Time > Value_Minute))
          {
            UVStatusNo = 1;
            DO1_Set;
          }
          else
          {
            UVStatusNo = 0;
            DO1_Reset;
          }
        }
      }
    }
  }
}

